        System.out.println("sin(1/4 PI) = " + Math.sin((1.0 / 4.0) * pi));
        System.out.println("sin(2/4 PI) = " + Math.sin((2.0 / 4.0) * pi));
        System.out.println("sin(3/4 PI) = " + Math.sin((3.0 / 4.0) * pi));
        System.out.println("sin(4/4 PI) = " + Math.sin((4.0 / 4.0) * pi));
        System.out.println("sin(5/4 PI) = " + Math.sin((5.0 / 4.0) * pi));
